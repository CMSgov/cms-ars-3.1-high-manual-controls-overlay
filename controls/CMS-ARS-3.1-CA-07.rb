  desc  "Control:
    The organization develops a continuous monitoring strategy and implements a
continuous monitoring program that includes: a. Establishment of defined
metrics (defined in the applicable system security plan) to be monitored based
on the organization security goals and objectives and in accordance with the
basic requirements set forth in NIST SP 800-137; b. Establishment of defined
frequencies (defined in the applicable system security plan), but no less than
once every 72 hours, for monitoring and defined frequencies (defined in the
applicable system security plan), but no less than once every 72 hours, for
assessments supporting such monitoring; c. Ongoing security control assessments
in accordance with the organizational continuous monitoring strategy; d.
Ongoing security status monitoring of organization-defined metrics in
accordance with the organizational continuous monitoring strategy; e.
Correlation and analysis of security-related information generated by
assessments and monitoring; f. Response actions to address results of the
analysis of security-related information; and g. Reporting the security status
of the organization and the information system to defined personnel or roles
(defined in the applicable system security plan) monthly.
