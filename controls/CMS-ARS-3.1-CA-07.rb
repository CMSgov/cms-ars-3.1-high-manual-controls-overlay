    Supplemental Guidance:
    Continuous monitoring programs facilitate ongoing awareness of threats,
vulnerabilities, and information security to support organizational risk
management decisions. The terms 'continuous' and
'ongoing' imply that organizations assess/analyze
security controls and information security-related risks at a frequency
sufficient to support organizational risk-based decisions. The results of
continuous monitoring programs generate appropriate risk response actions by
organizations. Continuous monitoring programs also allow organizations to
maintain the security authorizations of information systems and common controls
over time in highly dynamic environments of operation with changing
mission/business needs, threats, vulnerabilities, and technologies. Having
access to security-related information on a continuing basis through
reports/dashboards gives organizational officials the capability to make more
effective and timely risk management decisions, including ongoing security
authorization decisions. Automation supports more frequent updates to security
authorization packages, hardware/software/firmware inventories, and other
system information. Effectiveness is further enhanced when continuous
monitoring outputs are formatted to provide information that is specific,
measurable, actionable, relevant, and timely. Continuous monitoring activities
are scaled in accordance with the security categories of information systems.

    Guidance for systems processing, storing, or transmitting PII (to include
PHI):

    The state of security controls can directly correlate to privacy risk.
Continuous monitoring supports the identification of issues that could result
in unauthorized access to sensitive information such as PII, data quality
issues, and other concerns, including privacy, that are supported by security
controls.

    Guidance for systems processing, storing, or transmitting PHI:

    Consider using automated tools and mechanisms for system activity review.
The effectiveness of continuous monitoring of various activities, for example,
failed or successful log-ins, inappropriate file access, detecting and
reporting on malicious code/viruses through network transmission, is enhanced
using approved automated tools.
